// Drafts live as JSON files in <corpusRoot>/.drafts/<id>.json. The directory
// is gitignored per the v1 spec, so drafts never leak into commits unless the
// maintainer explicitly stages them via the stage endpoint, which projects
// the draft into primitives/<kind>/<slug>.json and removes the draft file.
//
// Each draft stores the UI-shape body (mutable, no validation required) plus
// envelope metadata (created/modified timestamps). This avoids re-projecting
// on every Save while the user types.

const draftsSubdir = ".drafts"

type draftEnvelope struct {
	ID         string         `json:"id"`
	Slug       string         `json:"slug,omitempty"` // mirror of body.slug for fast list
	Kind       string         `json:"kind,omitempty"`
	Title      string         `json:"title,omitempty"`
	CreatedAt  time.Time      `json:"created_at"`
	ModifiedAt time.Time      `json:"modified_at"`
	Body       map[string]any `json:"body"`
}

func (s *Server) draftsDir() string {
	return filepath.Join(s.CorpusRoot, draftsSubdir)
}

func (s *Server) ensureDraftsDir() error {
	dir := s.draftsDir()
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	// Always also ensure .gitignore inside .drafts/ so drafts directory is
	// never tracked even if the user forgets to add it at the repo root.
	gi := filepath.Join(dir, ".gitignore")
	if _, err := os.Stat(gi); os.IsNotExist(err) {
		_ = os.WriteFile(gi, []byte("# Auto-generated by commons; drafts are local-only.\n*\n!.gitignore\n"), 0o644)
	}
	return nil
}

func (s *Server) draftPath(id string) string {
	safe := filepath.Base(id) // disallow traversal
	return filepath.Join(s.draftsDir(), safe+".json")
}

// readDraft loads a single envelope by id, or (nil, nil) if absent.
func (s *Server) readDraft(id string) (*draftEnvelope, error) {
	b, err := os.ReadFile(s.draftPath(id))
	if err != nil {
		if os.IsNotExist(err) {
			return nil, nil
		}
		return nil, err
	}
	var env draftEnvelope
	if err := json.Unmarshal(b, &env); err != nil {
		return nil, err
	}
	return &env, nil
}

func (s *Server) writeDraft(env *draftEnvelope) error {
	if err := s.ensureDraftsDir(); err != nil {
		return err
	}
	env.ModifiedAt = time.Now().UTC()
	return atomicWriteJSON(s.draftPath(env.ID), env)
}

// newDraftID assembles a sortable id from the current timestamp plus a short
// random suffix. Avoids the UUID dependency footprint; collision odds are
// negligible for a single-user tool.
func newDraftID() string {
	now := time.Now().UTC()
	return fmt.Sprintf("draft-%s-%x", now.Format("20060102-150405"), now.UnixNano()%0xffff)
}

